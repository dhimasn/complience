    <p>Kebijakan Privasi Aplikasi Pengawasan SKEM</p>
    <p>Salah satu prioritas utama kami adalah privasi pengguna kami. Dokumen Kebijakan Privasi ini berisi jenis
        informasi yang dikumpulkan dan dicatat oleh Pengawasan SKEM dan bagaimana kami menggunakannya.</p>
    <p>Jika Anda memiliki pertanyaan tambahan atau memerlukan informasi lebih lanjut tentang Kebijakan Privasi kami,
        jangan ragu untuk menghubungi kami.</p>
    <p>File Log<br />Pengawasan SKEM mengikuti prosedur standar penggunaan file log. File-file ini mencatat pengunjung
        ketika mereka mengunjungi situs web. Informasi yang dikumpulkan oleh file log termasuk alamat protokol internet
        (IP), jenis browser, Penyedia Layanan Internet (ISP), cap tanggal dan waktu, halaman rujukan/keluar, dan mungkin
        jumlah klik. Ini tidak terkait dengan informasi apa pun yang dapat diidentifikasi secara pribadi. Tujuan dari
        informasi ini adalah untuk menganalisis tren, mengelola situs, melacak pergerakan pengguna di situs web, dan
        mengumpulkan informasi demografis. Kebijakan Privasi kami dibuat dengan bantuan Generator Kebijakan Privasi.</p>
    <p>Kebijakan Privasi<br />Perhatikan bahwa Pengawasan SKEM tidak memiliki akses atau kontrol terhadap cookie ini
        yang digunakan untuk memberikan pengalaman lebih personal pada anda.</p>
    <p>Kebijakan Privasi Pihak Ketiga<br />Kebijakan Privasi Pihak Ketiga, kami menyarankan Anda untuk berkonsultasi
        dengan Kebijakan Privasi masing-masing dari server pihak ketiga untuk informasi lebih rinci. Ini mungkin
        termasuk praktik dan instruksi mereka tentang cara memilih keluar dari opsi tertentu.</p>
    <p>Anda dapat memilih untuk menonaktifkan cookie melalui opsi browser individual Anda. Untuk mengetahui informasi
        lebih rinci tentang manajemen cookie dengan browser web tertentu, dapat ditemukan di situs web masing-masing
        browser.</p>
    <p>Informasi Anak<br />Bagian lain dari prioritas kami adalah menambahkan perlindungan bagi anak-anak saat
        menggunakan internet. Kami mendorong orang tua dan wali untuk mengamati, berpartisipasi, dan/atau memantau dan
        memandu aktivitas online mereka.</p>
    <p>Pengawasan SKEM tidak secara sadar mengumpulkan Informasi Identifikasi Pribadi apa pun dari anak di bawah 13
        tahun. Jika Anda merasa anak Anda memberikan informasi semacam ini di situs web kami, kami sangat menganjurkan
        Anda untuk menghubungi kami segera dan kami akan melakukan upaya terbaik kami untuk segera menghapus informasi
        tersebut dari catatan kami.</p>
    <p>Hanya Kebijakan Privasi Online<br />Kebijakan Privasi ini hanya berlaku untuk aktivitas online kami dan berlaku
        untuk pengunjung situs web kami sehubungan dengan informasi yang mereka bagikan dan/atau kumpulkan di Pengawasan
        SKEM. Kebijakan ini tidak berlaku untuk informasi apa pun yang dikumpulkan secara offline atau melalui saluran
        selain situs web ini.</p>
    <p>Izin<br />Dengan menggunakan aplikasi kami, Anda dengan ini menyetujui Kebijakan Privasi kami dan menyetujui
        Syarat dan Ketentuannya.</p>
